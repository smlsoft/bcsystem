import 'dart:io';

import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:smlaicloud/bloc/product_group/product_group_bloc.dart';
import 'package:smlaicloud/global.dart' as global;
import 'package:smlaicloud/model/product_group_model.dart';

class ProductGroupSearchScreen extends StatefulWidget {
  const ProductGroupSearchScreen({Key? key, required this.word}) : super(key: key);
  final String word;

  @override
  State<ProductGroupSearchScreen> createState() => ProductGroupSearchScreenState();
}

class ProductGroupSearchScreenState extends State<ProductGroupSearchScreen> with SingleTickerProviderStateMixin {
  TextEditingController searchController = TextEditingController();
  FocusNode searchFocusNode = FocusNode(skipTraversal: true);
  ScrollController listScrollController = ScrollController();
  String searchText = "";
  List<ProductGroupModel> productGroupListData = [];
  bool isKeyUp = false;
  bool isKeyDown = false;
  String selectGuid = "";
  int currentListIndex = 0;
  final _debouncer = global.Debouncer(1000);

  void setSystemLanguageList() async {
    await global.setSystemLanguage(context);

    loadDataList(searchText);
  }

  @override
  void initState() {
    setSystemLanguageList();
    listScrollController.addListener(onScrollList);
    searchText = widget.word;
    searchController.text = searchText;

    super.initState();
  }

  void loadDataList(String search) {
    context.read<ProductGroupBloc>().add(ProductGroupLoadList(offset: (productGroupListData.isEmpty) ? 0 : productGroupListData.length, limit: global.loadDataPerPage, search: search));
  }

  void onScrollList() {
    if (listScrollController.offset >= listScrollController.position.maxScrollExtent && !listScrollController.position.outOfRange) {
      loadDataList(searchText);
    }
  }

  @override
  void dispose() {
    listScrollController.dispose();
    searchController.dispose();
    super.dispose();
  }

  Widget listScreen({bool mobileScreen = false}) {
    return Scaffold(
      resizeToAvoidBottomInset: true,
      appBar: AppBar(
        backgroundColor: global.theme.appBarColor,
        automaticallyImplyLeading: false,
        title: Text(global.language('product_group')),
        leading: IconButton(
          focusNode: FocusNode(skipTraversal: true),
          icon: const Icon(Icons.arrow_back),
          onPressed: () {
            Navigator.pop(context, global.SearchCodeNameModel(code: "", names: [], isCancel: true, guidfixed: ''));
          },
        ),
      ),
      body: Focus(
          focusNode: FocusNode(skipTraversal: true, canRequestFocus: true),
          onKey: (node, event) {
            if (kIsWeb || Platform.isWindows || Platform.isLinux || Platform.isMacOS) {
              if (event is RawKeyDownEvent) {
                // print(event.logicalKey.keyLabel);
                if (event.logicalKey == LogicalKeyboardKey.escape) {
                  Navigator.pop(context, global.SearchCodeNameModel(code: "", names: [], isCancel: true, guidfixed: ''));
                }
                if (event.logicalKey == LogicalKeyboardKey.tab) {
                  if (selectGuid != "") {
                    Navigator.pop(context, global.SearchCodeNameModel(code: productGroupListData[currentListIndex].code, names: productGroupListData[currentListIndex].names, isCancel: false, guidfixed: ''));
                  }
                }
                if (event.logicalKey == LogicalKeyboardKey.arrowUp) {
                  isKeyDown = false;
                  int index = productGroupListData.indexOf(productGroupListData.firstWhere((element) => element.guidfixed == selectGuid));
                  if (index > 0) {
                    selectGuid = productGroupListData[index - 1].guidfixed;
                    isKeyUp = true;
                  }
                  setState(() {});
                }
                if (event.logicalKey == LogicalKeyboardKey.arrowDown) {
                  isKeyUp = false;
                  int index = productGroupListData.indexOf(productGroupListData.firstWhere((element) => element.guidfixed == selectGuid));
                  if (index < productGroupListData.length - 1) {
                    selectGuid = productGroupListData[index + 1].guidfixed;
                  }
                  isKeyDown = true;
                  setState(() {});
                }
              }
            }
            return KeyEventResult.ignored;
          },
          child: Column(
            children: [
              Container(
                  padding: const EdgeInsets.all(5),
                  color: global.theme.appBarColor,
                  child: Container(
                      padding: const EdgeInsets.all(5),
                      decoration: BoxDecoration(
                        color: Colors.white,
                        borderRadius: BorderRadius.circular(2),
                        boxShadow: [
                          BoxShadow(
                            color: Colors.grey.withOpacity(0.5),
                            spreadRadius: 5,
                            blurRadius: 7,
                            offset: const Offset(0, 2),
                          ),
                        ],
                      ),
                      child: Padding(
                          padding: const EdgeInsets.only(left: 10, right: 10),
                          child: TextFormField(
                              onFieldSubmitted: (value) {
                                searchFocusNode.requestFocus();
                              },
                              onChanged: (value) {
                                try {
                                  _debouncer.run(() {
                                    setState(() {
                                      productGroupListData = [];
                                    });
                                    loadDataList(value);
                                  });
                                } catch (_) {}
                              },
                              autofocus: true,
                              focusNode: searchFocusNode,
                              controller: searchController,
                              decoration: InputDecoration(
                                isDense: true,
                                contentPadding: const EdgeInsets.only(top: 10, bottom: 10),
                                border: InputBorder.none,
                                hintText: global.language('search'),
                              ))))),
              Container(
                  padding: const EdgeInsets.only(left: 10, right: 10, top: 5, bottom: 5),
                  decoration: BoxDecoration(
                      color: global.theme.columnHeaderColor,
                      border: const Border(
                        bottom: BorderSide(width: 1.0, color: Colors.grey),
                      )),
                  child: Row(children: [
                    Expanded(flex: 5, child: Text(global.language("product_group_code"), style: const TextStyle(color: Colors.black, fontWeight: FontWeight.bold))),
                    Expanded(
                        flex: 10,
                        child: Text(
                          global.language("product_group_name"),
                          style: const TextStyle(color: Colors.black, fontWeight: FontWeight.bold),
                          maxLines: 2,
                          overflow: TextOverflow.ellipsis,
                        )),
                  ])),
              Expanded(child: SingleChildScrollView(controller: listScrollController, child: Column(children: productGroupListData.map((value) => listObject(value)).toList())))
            ],
          )),
    );
  }

  Widget listObject(ProductGroupModel value) {
    return GestureDetector(
        onTap: () {
          Navigator.pop(context, global.SearchCodeNameModel(code: value.code, names: value.names, isCancel: false, guidfixed: ''));
        },
        child: Container(
            decoration: BoxDecoration(
              color: (selectGuid == value.guidfixed) ? Colors.cyan[100] : Colors.white,
              border: const Border(
                bottom: BorderSide(width: 1.0, color: Colors.grey),
              ),
            ),
            padding: const EdgeInsets.only(left: 10, right: 10, top: 5, bottom: 5),
            child: Row(crossAxisAlignment: CrossAxisAlignment.start, children: [
              Expanded(flex: 5, child: Text(value.code, maxLines: 2, overflow: TextOverflow.ellipsis)),
              Expanded(
                  flex: 10,
                  child: Text(
                    global.packName(value.names),
                    maxLines: 2,
                    overflow: TextOverflow.ellipsis,
                  )),
            ])));
  }

  @override
  Widget build(BuildContext context) {
    for (int i = 0; i < productGroupListData.length; i++) {
      if (productGroupListData[i].guidfixed == selectGuid) {
        currentListIndex = i;
        break;
      }
    }
    return Scaffold(
        resizeToAvoidBottomInset: true,
        body: LayoutBuilder(builder: (context, constraints) {
          return BlocListener<ProductGroupBloc, ProductGroupState>(
              listener: (context, state) {
                // Load
                if (state is ProductGroupLoadSuccess) {
                  setState(() {
                    if (state.productGroups.isNotEmpty) {
                      productGroupListData.addAll(state.productGroups);
                      if (productGroupListData.isNotEmpty) {
                        selectGuid = productGroupListData[0].guidfixed;
                      } else {
                        selectGuid = "";
                      }
                    }
                  });
                }
              },
              child: (constraints.maxWidth > 800) ? listScreen(mobileScreen: false) : listScreen(mobileScreen: true));
        }));
  }
}
